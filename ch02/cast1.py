print(int(3.8))     # 실수를 정수로 변환
print(float(3))     # 실수로 변환
print("2" + "3")    # 문자까리 연산은 문자열 연결 -> 문자열 23출력
print(int("2") + int("3"))
print("1.1" + "2.5")
print(float("1.1") + float("2.5"))
print("나이는 "+str(7)+"살입니다")     # str은 문자로 변환
print("나이는 ",7,"살입니다")          # ,를 이용해 연결할 때는 한칸씩 공백(구분자)이 생긴다